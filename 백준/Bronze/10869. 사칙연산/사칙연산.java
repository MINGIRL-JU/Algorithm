import java.util.Scanner;

public class Main {
	static int A, B;

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		A = sc.nextInt(); B = sc.nextInt();
		System.out.println(A+B);
		System.out.println(A-B);
		System.out.println(A*B);
		System.out.println(A/B);
		System.out.println(A%B);
	}

}
